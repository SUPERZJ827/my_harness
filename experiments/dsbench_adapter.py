import csv
import json
import re
from pathlib import Path
from typing import Any


DSBENCH_PREFIX = "dsbench_da_"


def is_dsbench_task(task_id: str) -> bool:
    return task_id.startswith(DSBENCH_PREFIX)


def dsbench_adapter_prompt() -> str:
    return """## DSBench Adapter Contract
This task uses the DSBench data-analysis format. Follow these dataset-specific rules:
1. Before solving, read `question_schema.json` if present. It defines whether `final_answer.txt` must be an option letter or an open answer.
2. Use `workbook_summary.md` and `csv_profile.md` as navigation aids before loading entire spreadsheets. They may be incomplete; inspect the original workbook/CSV for exact values.
3. For workbook questions, prefer targeted `openpyxl` inspection of relevant sheets, formulas, dimensions, and cell ranges over broad repeated full-workbook scans.
4. For multiple-choice questions, compute the underlying value internally, then map it to the option letter in `question_schema.json` or the question text.
5. If the computed result matches an option value such as `72`, `$34,274,780`, `123.4%`, or a text label, write only the corresponding uppercase option letter.
6. Do not write explanations, code fences, debug text, intermediate values, or markdown into `final_answer.txt`.
7. If the task is open-answer, write only the requested final value/name/string. Normalize obvious whitespace, but do not add commentary.
8. Keep the solution bounded. If the workbook is large, inspect summaries and relevant sheets first, then compute only what the question asks.

"""


def _safe_text(value: Any, max_len: int = 100) -> str:
    text = "" if value is None else str(value)
    text = " ".join(text.split())
    return text[:max_len]


def ensure_adapter_files(run_dir: str | Path) -> list[str]:
    """Create lightweight DSBench helper files if older task folders lack them."""
    run_path = Path(run_dir)
    created: list[str] = []
    if not (run_path / "workbook_summary.md").exists():
        if _generate_workbook_summary(run_path):
            created.append("workbook_summary.md")
    if not (run_path / "csv_profile.md").exists():
        if _generate_csv_profile(run_path):
            created.append("csv_profile.md")
    return created


def _generate_workbook_summary(run_path: Path) -> bool:
    workbook_paths = sorted(
        list(run_path.glob("*.xlsx")) + list(run_path.glob("*.xlsm")) + list(run_path.glob("*.xls"))
    )
    if not workbook_paths:
        return False
    lines = [
        "# Workbook Summary",
        "",
        "Generated by the DSBench runtime adapter. Use as a navigation aid; inspect original workbooks for exact calculations.",
        "",
    ]
    try:
        from openpyxl import load_workbook
    except Exception as exc:
        lines.append(f"Could not import openpyxl: {exc}")
        (run_path / "workbook_summary.md").write_text("\n".join(lines) + "\n", encoding="utf-8")
        return True

    for workbook in workbook_paths:
        lines.extend([f"## {workbook.name}", ""])
        try:
            wb_values = load_workbook(workbook, data_only=True, read_only=True)
            wb_formulas = load_workbook(workbook, data_only=False, read_only=True)
        except Exception as exc:
            lines.extend([f"- Could not open workbook: {exc}", ""])
            continue
        lines.append(f"- Sheets: {', '.join(wb_values.sheetnames)}")
        for sheet_name in wb_values.sheetnames:
            ws_values = wb_values[sheet_name]
            ws_formulas = wb_formulas[sheet_name]
            lines.extend(
                [
                    "",
                    f"### Sheet: {sheet_name}",
                    f"- Dimensions: rows={ws_values.max_row}, cols={ws_values.max_column}",
                    "- Top-left preview:",
                ]
            )
            for idx, row in enumerate(
                ws_values.iter_rows(
                    min_row=1,
                    max_row=min(ws_values.max_row, 10),
                    min_col=1,
                    max_col=min(ws_values.max_column, 10),
                    values_only=True,
                ),
                start=1,
            ):
                lines.append(f"  - R{idx}: {[_safe_text(value, 80) for value in row]}")
            formulas = []
            for row in ws_formulas.iter_rows():
                for cell in row:
                    value = cell.value
                    if isinstance(value, str) and value.startswith("="):
                        formulas.append(f"{cell.coordinate}={_safe_text(value, 120)}")
                    if len(formulas) >= 20:
                        break
                if len(formulas) >= 20:
                    break
            if formulas:
                lines.append("- Formula examples:")
                for formula in formulas:
                    lines.append(f"  - {formula}")
        lines.append("")
    (run_path / "workbook_summary.md").write_text("\n".join(lines) + "\n", encoding="utf-8")
    return True


def _generate_csv_profile(run_path: Path) -> bool:
    csv_paths = sorted(path for path in run_path.glob("*.csv") if path.name != "csv_profile.md")
    if not csv_paths:
        return False
    lines = [
        "# CSV Profile",
        "",
        "Generated by the DSBench runtime adapter. Use as a quick structure preview.",
        "",
    ]
    for csv_path in csv_paths:
        lines.extend([f"## {csv_path.name}", ""])
        rows = []
        for encoding in ("utf-8-sig", "latin-1"):
            try:
                with csv_path.open("r", encoding=encoding, newline="") as handle:
                    rows = [row for _, row in zip(range(10), csv.reader(handle))]
                break
            except Exception:
                rows = []
        if not rows:
            lines.extend(["- Could not read CSV or file is empty.", ""])
            continue
        lines.append(f"- First row: {[_safe_text(value, 80) for value in rows[0][:12]]}")
        lines.append("- Row preview:")
        for idx, row in enumerate(rows[1:8], start=2):
            lines.append(f"  - R{idx}: {[_safe_text(value, 80) for value in row[:12]]}")
        lines.append("")
    (run_path / "csv_profile.md").write_text("\n".join(lines) + "\n", encoding="utf-8")
    return True


def normalize_final_answer(run_dir: str | Path) -> dict[str, Any]:
    run_path = Path(run_dir)
    final_path = run_path / "final_answer.txt"
    schema = _load_schema(run_path)
    result: dict[str, Any] = {
        "adapter": "dsbench",
        "final_answer_exists": final_path.exists(),
        "changed": False,
        "reason": "",
    }
    if not final_path.exists():
        result["reason"] = "missing_final_answer"
        _write_report(run_path, result)
        return result

    original = final_path.read_text(encoding="utf-8", errors="ignore")
    normalized = normalize_answer_text(original, schema)
    result["original"] = original
    result["normalized"] = normalized
    if normalized != original:
        final_path.write_text(normalized, encoding="utf-8")
        result["changed"] = True
        result["reason"] = "normalized_final_answer"
    else:
        result["reason"] = "already_normalized"
    _write_report(run_path, result)
    return result


def normalize_answer_text(answer: str, schema: dict[str, Any] | None) -> str:
    text = _strip_answer_noise(answer)
    if schema and schema.get("requires_option_letter"):
        options = {str(k).upper(): str(v) for k, v in (schema.get("options") or {}).items()}
        letter = _extract_option_letter(text, options)
        if letter:
            return letter
    return text.strip()


def _load_schema(run_path: Path) -> dict[str, Any] | None:
    schema_path = run_path / "question_schema.json"
    if not schema_path.exists():
        return None
    try:
        return json.loads(schema_path.read_text(encoding="utf-8"))
    except Exception:
        return None


def _strip_answer_noise(text: str) -> str:
    text = text.strip()
    text = re.sub(r"```(?:text|python|json)?", "", text, flags=re.I).replace("```", "")
    text = re.sub(r"(?i)^final answer\s*[:：]\s*", "", text.strip())
    text = re.sub(r"(?i)^answer\s*[:：]\s*", "", text.strip())
    return " ".join(text.split())


def _extract_option_letter(text: str, options: dict[str, str]) -> str | None:
    stripped = text.strip()
    upper = stripped.upper()
    if re.fullmatch(r"[A-I]", upper):
        return upper

    option_match = re.search(r"(?i)\b(?:option|choice|answer)\s*[:：]?\s*([A-I])\b", stripped)
    if option_match:
        return option_match.group(1).upper()

    paren_match = re.search(r"\(([A-I])\)", stripped.upper())
    if paren_match:
        return paren_match.group(1).upper()

    exact = _canonical_value(stripped)
    for letter, value in options.items():
        if exact and exact == _canonical_value(value):
            return letter

    numeric = _to_float(stripped)
    if numeric is not None:
        for letter, value in options.items():
            option_numeric = _to_float(value)
            if option_numeric is not None and abs(numeric - option_numeric) <= max(1e-6, abs(option_numeric) * 1e-6):
                return letter

    return None


def _canonical_value(value: str) -> str:
    return re.sub(r"[^a-z0-9.+%-]", "", value.lower())


def _to_float(value: str) -> float | None:
    cleaned = value.strip()
    cleaned = re.sub(r"(?i)\b(answer|option|choice)\b", "", cleaned)
    cleaned = cleaned.replace("$", "").replace(",", "").replace("%", "").strip()
    match = re.search(r"[-+]?\d+(?:\.\d+)?", cleaned)
    if not match:
        return None
    try:
        return float(match.group(0))
    except ValueError:
        return None


def _write_report(run_path: Path, result: dict[str, Any]) -> None:
    (run_path / "dsbench_adapter_postprocess.json").write_text(
        json.dumps(result, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
